package Java;

public class PrimitiveExample {
    public static void main(String[] args) {
        // Integer type
        int age = 25; // Stores whole numbers
        System.out.println("Age: " + age);

        // Floating-point type
        float price = 99.99f; // Stores decimal numbers
        System.out.println("Price: " + price);

        // Character type
        char grade = 'A'; // Stores a single character
        System.out.println("Grade: " + grade);

        // Boolean type
        boolean isPassed = true; // Stores true or false
        System.out.println("Passed: " + isPassed);

        // Byte type
        byte smallNumber = 100; // Stores small numbers (-128 to 127)
        System.out.println("Small Number: " + smallNumber);

        // Short type
        short mediumNumber = 30000; // Stores medium range numbers
        System.out.println("Medium Number: " + mediumNumber);

        // Long type
        long largeNumber = 123456789L; // Stores large whole numbers
        System.out.println("Large Number: " + largeNumber);

        // Double type
        double preciseDecimal = 12345.6789; // Stores precise decimal numbers
        System.out.println("Precise Decimal: " + preciseDecimal);
    }


}


